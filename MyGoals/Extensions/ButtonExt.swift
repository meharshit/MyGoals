import UIKit


extension UIButton{
    
    func selectedColour(){
        self.backgroundColor = #colorLiteral(red: 0.3019607843, green: 0.4549019608, blue: 0.6666666667, alpha: 1)
        
    }
    
    func deSelectedColour(){
        self.backgroundColor = #colorLiteral(red: 0.4341950663, green: 0.6547021298, blue: 0.948227316, alpha: 1)
        
    }
    
    
    
    
}
